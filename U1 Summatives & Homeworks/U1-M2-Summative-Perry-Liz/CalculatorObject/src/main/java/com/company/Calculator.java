package com.company;

public class Calculator {

    // perform int calculations

    public void additionInt (int num1, int num2) {

        int answerInt = num1 + num2;

        System.out.println(num1 + " + " + num2 + " = " + answerInt);

    }

    public void subtractionInt (int num1, int num2) {

        int answerInt = num1 - num2;

        System.out.println(num1 + " - " + num2 + " = " + answerInt );

    }

    public void multiplicationInt (int num1, int num2) {

        int answerInt = num1 * num2;

        System.out.println(num1 + " * " + num2 + " = " + answerInt);

    }

    public void divisionInt (int num1, int num2) {

        int answerInt = num1 / num2;

        System.out.println(num1 + " / " + num2 + " = " + answerInt);

    }

    // perform double calculations

    public void additionDouble (double num1, double num2) {

        double answerInt = num1 + num2;

        System.out.println(num1 + " + " + num2 + " = " + answerInt);

    }

    public void subtractionDouble (double num1, double num2) {

        double answerInt = num1 - num2;

        System.out.println(num1 + " - " + num2 + " = " + answerInt );

    }

    public void multiplicationDouble (double num1, double num2) {

        double answerInt = num1 * num2;

        System.out.println(num1 + " * " + num2 + " = " + answerInt);

    }

    public void divisionDouble (double num1, double num2) {

        double answerInt = num1 / num2;

        System.out.println(num1 + " / " + num2 + " = " + answerInt);

    }
}
