package com.company;

public class Transmission {

    private int gears;
    private String model;

    public Transmission() {

    }

    public int getGears() {

        return gears;

    }

    public void setGears(int gears) {

        this.gears = gears;

    }

    public String getModel() {

        return model;

    }

    public void setModel() {

        this.model = model;

    }

}
