package com.company;

public class Calculator {

    public static int add(int integer1, int integer2) {

        return integer1 + integer2;

    }

    public static int subtract(int integer1, int integer2) {

        return integer1 - integer2;
    }

    public static int multiply(int integer1, int integer2) {

        return integer1 * integer2;

    }

    public static int divide(int integer1, int integer2) {

        return integer1 / integer2;
    }

}
